    private void solveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solveButtonActionPerformed
        heirs.clear();
        heirs.resetRemainingQuota();
        if(this.fatherComboBox.getModel().getSelectedItem().equals("yes")) {
            heirs.addHeir("father", 1);
        }
        if(this.motherComboBox.getModel().getSelectedItem().equals("yes")) {
            heirs.addHeir("mother", 1);
        }
        if(this.husbandComboBox.getModel().getSelectedItem().equals("yes")) {
           heirs.addHeir("husband", 1);
        }
        if(this.wifeComboBox.getModel().getSelectedItem().equals("yes")) {
            heirs.addHeir("wife", 1);
        }
        if(!this.daughterField.getText().isEmpty()) {
            int daugthers = Integer.valueOf(this.daughterField.getText());
            
            if(daugthers > 0) {
                heirs.addHeir("daugther", daugthers);
            }
        }
        if(!this.sonField.getText().isEmpty()) {
            int sons = Integer.valueOf(this.sonField.getText());
            if(sons > 0) {
                heirs.addHeir("son", sons);
            }
        }
        
        filter.start();
        
        for(int i = 0 ;i<heirs.getSize(); i++)
            System.out.println(heirs.getHeir(i).getDegree() + " " + heirs.getHeir(i).getQuota());
        
        System.out.println("Remaining quota : " + Heir.m_remaining_quota);
    }//GEN-LAST:event_solveButtonActionPerformed

